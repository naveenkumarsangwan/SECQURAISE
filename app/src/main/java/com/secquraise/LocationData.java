package com.secquraise;

public class LocationData {

    private String captureCount;
    private String connectivity;
    private String bCharging;
    private String frequency;
    private String bCharge;
    private String location;

    public String getCaptureCount() {
        return captureCount;
    }

    public void setCaptureCount(String captureCount) {
        this.captureCount = captureCount;
    }

    public String getConnectivity() {
        return connectivity;
    }

    public void setConnectivity(String connectivity) {
        this.connectivity = connectivity;
    }

    public String getbCharging() {
        return bCharging;
    }

    public void setbCharging(String bCharging) {
        this.bCharging = bCharging;
    }

    public String getFrequency() {
        return frequency;
    }

    public void setFrequency(String frequency) {
        this.frequency = frequency;
    }

    public String getbCharge() {
        return bCharge;
    }

    public void setbCharge(String bCharge) {
        this.bCharge = bCharge;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }


}
